public class Matematicas {
	public static boolean esPar(int numero) {
		if (numero %2 == 0)
			return true;
		return false;
	}

	public static boolean esDivisiblePorTres(int numero) {
		if (numero %3 == 0)
			return true;
		return false;
	}

	public static boolean esDivisiblePorCinco(int numero) {
		if (numero %5 == 0)
			return true;
		return false;
	}
}


